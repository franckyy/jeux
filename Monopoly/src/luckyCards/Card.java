package luckyCards;

public class Card {
	private String description;
	private int moneyValue;
	
	/**
	 * A Card has a description on it and either a negative or positive monetary value.
	 * @param _description
	 * @param _moneyValue
	 */
	public Card(String _description, int _moneyValue){
		moneyValue = _moneyValue;
		description = _description;
	}
	public String getDescription() {
		return description;
	}
	public int getMoneyValue() {
		return moneyValue;
	}

}
